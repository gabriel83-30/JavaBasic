package git;

public class GitTheory {
    // GIT
    // GIT = este un sistem distribuit de control al versiunilor;
    // Este folosit pentru a urmari si gestiona toate modificarile facute in fisierele unui proiect;
    // Este folosit in  special pt codul sursa, dar poate fi folosit pt orice fisier;
    // Salveaza istoricul modificarilor din proiect, permite revenirea la orice versiune anterioara a unui sistem;
    // Permiyte lucru atat individual cat si colaborativ;


    // GIT HUB
    // Este o platforma online care gazduieste proiecte ce folosesc GIT;
    // Permite colaborarea cu alti dezvoltatori indiferent unde se afla ei ;


    //  GIT- unealta locala;
    //  GIT HUB - platforma online;

    // Termeni de baza :
    // Repository(Repo) - proiectul"tau" gestionat cu GIT;
    // Commit - salvarea unui set modificari cu un mesaj explicativ:
    // Branch - o ramura pe care poti dezvolta functionalitati fara a afecta codul principal
    // Merge - combinarea modificarilor dintr-o ramura in alta:
    // Push - trimiterea modificarilor locale catre un repository remote(GIT LOCAL ---> GIT HUB REMOTE);
    // Pull- descarcarea ultimelor modificari de pe remote pe local(GIT HUB REMOTE ---> GIT LOCAL);

}
